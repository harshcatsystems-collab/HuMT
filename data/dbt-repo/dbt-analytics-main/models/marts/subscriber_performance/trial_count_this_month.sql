SELECT
  DATE_TRUNC(
    'month',
    "DBT_CORE"."FCT_USER_SUBSCRIPTION_HISTORY"."CREATED_AT_IST"
  ) AS "CREATED_AT_IST",
  count(
    distinct "DBT_CORE"."FCT_USER_SUBSCRIPTION_HISTORY"."USER_ID"
  ) AS "Trial Count Past 30 Days"
FROM
  "DBT_CORE"."FCT_USER_SUBSCRIPTION_HISTORY"
WHERE
  "DBT_CORE"."FCT_USER_SUBSCRIPTION_HISTORY"."PLAN_CATEGORY" = 'Trial'
GROUP BY
  DATE_TRUNC(
    'month',
    "DBT_CORE"."FCT_USER_SUBSCRIPTION_HISTORY"."CREATED_AT_IST"
  )